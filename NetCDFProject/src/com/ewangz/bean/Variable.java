package com.ewangz.bean;

/**
 * @date 2014-01-06
 * @author qincd
 * @功能   自定义Variable实体类
 */
public class Variable {
	private String shortName;

	public String getShortName() {
		return shortName;
	}

	public void setShortName(String shortName) {
		this.shortName = shortName;
	}
	
	public String toString() {
		return shortName + "";
	}
}
